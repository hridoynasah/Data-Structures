#include <bits/stdc++.h>
using namespace std;

int main()
{
    // Integer to Character.
    int a = 71;
    char c = char(a); // G == 71
    cout << "Character value of integer 71: " << c << endl;

    // Character to integer.
    int x = char('c'); // c == 99
    cout << "Integer value of character c: " << x << endl;

    return 0;
}